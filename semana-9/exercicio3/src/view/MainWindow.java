    private void cadastrarPessoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarPessoaActionPerformed
        if(!(nome.getText().equals("")) && !(sobrenome.getText().equals("")) && !(cpf.getText().equals("")) && !(idade.getText().equals(""))){
            if(masculino.isSelected()||feminino.isSelected()){
                try {
                    FileWriter file = new FileWriter("pessoas.txt");
                    PrintWriter fileWriter = new PrintWriter(file);
                    
                    String nomePessoa = nome.getText();
                    String sobrenomePessoa = sobrenome.getText();
                    Integer idadePessoa = Integer.parseInt(idade.getText());
                    String cpfPessoa = cpf.getText();
                    String sexo;
                    if(masculino.isSelected()){
                        sexo = "masculino";            
                    }else{
                        sexo = "feminino";
                    }
                    Pessoa pessoa = new Pessoa(nomePessoa, sobrenomePessoa, idadePessoa, cpfPessoa, sexo);
                    fileWriter.println(pessoa);
                } catch (IOException ex) {
                    Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Selecione o sexo.");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Todos os campos s√£o obrigat√≥rios.");
        }
    }//GEN-LAST:event_cadastrarPessoaActionPerformed

