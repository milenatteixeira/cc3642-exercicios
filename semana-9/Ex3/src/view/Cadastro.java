    private void CadastraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastraActionPerformed
       if (Nome.getText().length()==0 || SobreNome.getText().length()==0 || Idade.getText().length()==0 || CPF.getText().length()==0 || Sexo.getSelection()==null) JOptionPane.showMessageDialog(null, "Favor completar os campos!","Erro", JOptionPane.ERROR_MESSAGE);
       else{
           String pessoa = Nome.getText()+":"+SobreNome.getText()+":"+Idade.getText()+":"+CPF.getText()+":";
           if (masculino.isSelected()) pessoa+="Masculino";
           else pessoa+="Feminino";
           System.out.println(pessoa);
           File file = new File("dados.txt");//Procurar arquivo
           if (file.exists()==false){//Verificar se existe
            try{
                FileWriter arquivo = new FileWriter("dados.txt");//Criar Arquivo
                PrintWriter escrever = new PrintWriter(arquivo);//Criar OBJ para escrever
                escrever.println(pessoa);//Escrever no arquivo
                arquivo.close();//Fechar Arquivo
            }
            catch(Exception e){}
           }
           else{
            try(FileWriter fw = new FileWriter("dados.txt", true);
                BufferedWriter bw = new BufferedWriter(fw);
                PrintWriter out = new PrintWriter(bw))
            {
                out.println(pessoa);
            }
            catch(Exception e){}
               
           }
       }
       Nome.setText("");
       SobreNome.setText("");
       Idade.setText("");
       CPF.setText("");
       Sexo.clearSelection();
    }//GEN-LAST:event_CadastraActionPerformed


